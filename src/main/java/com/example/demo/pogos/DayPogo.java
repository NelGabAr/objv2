package com.example.demo.pogos;

public class DayPogo {
    
    private Long totalSells;
    private Long morningSells;
    private Long afternoonSells;

    public DayPogo(){}

    public Long getTotalSells() {
        return totalSells;
    }

    public void setTotalSells(Long totalSells) {
        this.totalSells = totalSells;
    }

    public Long getMorningSells() {
        return morningSells;
    }

    public void setMorningSells(Long morningSells) {
        this.morningSells = morningSells;
    }

    public Long getAfternoonSells() {
        return afternoonSells;
    }

    public void setAfternoonSells(Long afternoonSells) {
        this.afternoonSells = afternoonSells;
    }

    
}
